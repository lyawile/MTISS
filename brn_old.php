                        <h1>Big Results Now</h1>
                        <div class="thick-hr-line wide-left"></div>
                        <p>
                            The Government of Tanzania has decided to prioritise the available resources to strategic sectors so as to realise 
                            the Big Results Now (BRN). The Education Sector is among the six prioritised sectors, which has developed <b>9</b> 
                            key initiatives to improve the quality of basic education and thereby increasing the pass rates in Primary and 
                            Secondary Schools. The <b>Official School Ranking</b> as per examination results according to the performance and improvement is among the 9 initiatives. In this ranking, 
                            schools are classified into bands of three major groups namely <b>Green, Yellow</b> and <b>Red</b> representing <b>High, Medium</b> and <b>Low</b> performing schools respectively. The schools' 
                            information will bring community awareness and engagement as well as improve transparency and accountability. 
                        </p>

